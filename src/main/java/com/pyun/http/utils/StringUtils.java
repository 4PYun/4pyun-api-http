	/**
	 * Split string to array by length
	 * @param str
	 * @param len
	 * @return
	 */
	public static String[] split(String str, int len) {
		if (str.length() <= len) return new String[] {str};
		
		return ArrayUtils.addAll(new String[] { str.substring(0, len) }, StringUtils.split(str.substring(len), len));
	}
	
	/**
	 * Split string to map
	 * @param str
	 * @param separator
	 * @param keyValueSeparator
	 * @return
	 */
	public static Map<String, String> splitMap(String str, String separator, String keyValueSeparator) {
		Map<String, String> map = new HashMap<String, String>();
		if (StringUtils.isBlank(str)) return map;
		String[] vks = str.split(separator);
		for (String kv : vks) {
			String[] args = kv.split(keyValueSeparator, 2);
			if (args.length == 2) {
				map.put(args[0], args[1]);
			} else if (args.length == 1) {
				map.put(args[0], null);
			}
		}
		return map;
	}
	
